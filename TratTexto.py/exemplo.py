x = 'abc_123_XYZ'

print(len(x))#O tamanho inteiro do conteúdo da variável 
print(x[4],x[9])#O conteúdo em determinada posição
print(x[0:3],x[4:7])#O conteúdo em um intervalo de tanto a tanto[inicio:final)
print(x[4:])#O conteúdo a partir da pos1 até o final do texto
print(x[:7],x[:3])#O conteúdo a partir do inicio da string até pos2-1  [inicio:final)
print(x[-3:])#O conteúdo dos n últimos caracteres
print(x[:-3])#O conteúdo sem  os n últimos
print(x[::-1])#O conteúdo invertido
